package logical_Program;

public class Patternm 
{
	public static void main(String[] args) 
	{
		for (int x=2;x<=4;x++)
		{
			for(int z=2;z<=x;z++)
			{
				
				System.out.print(" ");	
			}
			for (int y=x;y<=4;y++)
			{
				System.out.print("* ");
			}
			System.out.println();
			
		}
	}
}



 




